    private void jButtonSumarAltitudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSumarAltitudActionPerformed
        int nuevaAltitud;
        nuevaAltitud = Integer.parseInt(jTextFieldAltitud.getText());
        nuevaAltitud++;
        if(nuevaAltitud<=40000) jTextFieldAltitud.setText(""+nuevaAltitud);

        try {
            controlador.modificarAltitud();
        } catch (IOException ex) {
            Logger.getLogger(Piloto.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButtonSumarAltitudActionPerformed

    private void jTextFieldAltitudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextFieldAltitudActionPerformed
               int valor = Integer.parseInt(jTextFieldAltitud.getText());
       
       if(valor<0 || valor>40000) JOptionPane.showMessageDialog(this, "La altitud ha de estar entre 0 y 40000", "Error", JOptionPane.ERROR_MESSAGE);
        
        // Tenemos que llamar al controlador 
       else    try {
           controlador.modificarAltitud();
               } catch (IOException ex) {
                   Logger.getLogger(Piloto.class.getName()).log(Level.SEVERE, null, ex);
               } 
    }//GEN-LAST:event_jTextFieldAltitudActionPerformed

    private void jButtonRestarAltitudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRestarAltitudActionPerformed
        int nuevaAltitud;
        nuevaAltitud = Integer.parseInt(jTextFieldAltitud.getText());
        nuevaAltitud--;
        if(nuevaAltitud>=0) jTextFieldAltitud.setText(""+nuevaAltitud);

        try {
            controlador.modificarAltitud();
        } catch (IOException ex) {
            Logger.getLogger(Piloto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonRestarAltitudActionPerformed

